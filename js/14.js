/* Funciones - Arrow Functions */

const sumar = (numero, numero2) => numero + numero2 // cuando veas que es '() =>' es por que es un Arrow Function

const sumaArrow = () => 2 + 2

const resultado = sumar(30, 30)
console.log(resultado)
console.log("")

const resultado2 = sumaArrow()
console.log(resultado2)